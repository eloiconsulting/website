	$(document).ready(function() {
		// alert("validate")
		$('#exampleModal').on('show.bs.modal', function (event) {
		// alert("validate")
			$("#submit").on('click' , function(){
			//alert("submit");
				var fname = $('#firstname').val();
				if(fname == '')
				{
					alert('First name is empty');return false;
				}
				var lname = $('#lastname').val();
				if(lname == '')
				{
					alert('Last name is empty');return false;
				}
				var email = $('#email').val();
				if(email == '')
				{
					alert('Email  is empty');return false;
				}
				var phone = $('#phone').val();
				if(phone == '')
				{
					alert('Phone No name is empty');return false;
				}
				var aplliedpost = $('#applyingpost').val();
				if(aplliedpost == '')
				{
					alert('Applied post field is empty');return false;
				}
				var exp = $('#experience').val();
				if(exp == '')
				{
					alert('experience field is empty');return false;
				}
				var cvrltr = $('#cvrltr').val();
				var resume = $('#resume').val();
				if(resume == '')
				{
					alert('Select file to upload');return false;
				}
			//alert(name);
				$.ajax({
					type : "POST",
					url : "<?php echo base_url();?>Homectrl/jobapply",
					dataType:"json",
					data : {fname : fname,lname :lname,email : email,phone : phone,aplliedpost : aplliedpost,exp : exp,cvrltr :cvrltr,resume : resume},
					success:function(data){
						console.log(data);
						toastr.success("Job Applied Successfully");
							setTimeout(function(){// wait for 5 secs(2)
					           location.reload(); 
					      }, 5000);
						}
					})
				return false;
			});	
		});
	});
	
</script>